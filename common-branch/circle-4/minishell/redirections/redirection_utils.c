#include "headers.h"

void		close_pipe(t_shell *sh)
{
	if (sh->pipe[0] != 0)
		close(sh->pipe[0]);
	if (sh->pipe[1] != 1)
		close(sh->pipe[1]);
	sh->pipe[0] = 0;
	sh->pipe[1] = 1;
}
