#include "HumanB.hpp"

HumanB::HumanB(std::string name)
{
	this->setName(name);
}

void	HumanB::setName(std::string name)
{
	this->name = name;
}

void	HumanB::setWeapon(Weapon &weapon)
{
	this->weapon = &weapon;
}

void	HumanB::attack()
{
	std::cout << this->name + " attacks with " + this->weapon->getType() << std::endl;
}
